"""
    Comparison operators: Comparison operators are used to compare values. It returns either True or False according to the condition. It is also called as relational operator. Such as:

        >	Greater than - True if left operand is greater than the right	x > y
        <	Less than - True if left operand is less than the right	x < y
        ==	Equal to - True if both operands are equal	x == y
        !=	Not equal to - True if operands are not equal	x != y
        >=	Greater than or equal to - True if left operand is greater than or equal to the right	x >= y
        <=	Less than or equal to - True if left operand is less than or equal to the right	x <= y


"""

x = 10
y = 12

# Output: x > y is False
print('x > y is', x > y)

# Output: x < y is True
print('x < y is', x < y)

# Output: x == y is False
print('x == y is', x == y)

# Output: x != y is True
print('x != y is', x != y)

# Output: x >= y is False
print('x >= y is', x >= y)

# Output: x <= y is True
print('x <= y is', x <= y)
