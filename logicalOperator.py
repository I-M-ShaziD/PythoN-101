"""
    Logical Operator: Logical operators are the and, or, not operators. 

    and	True if both the operands are true	x and y
    or	True if either of the operands is true	x or y
    not	True if operand is false (complements the operand)	not x

"""

x = True
y = False

print('x and y is', x and y)

print('x or y is', x or y)

print('not x is', not x)


# AND Truth Table:

"""
  A	      B	   A and B
True	True	True
True	False	False
False	True	False
False	False	False

"""

# OR Truth Table:

"""
  A	      B	   A or B
True	True	True
True	False	True
False	True	True
False	False	False

"""

# Not Truth Table:

"""

A       not A
True	False
False	True

"""
