"""

    Arithmatic Operators:


    Defination: Arithmetic operators are used to perform mathematical operations like addition, subtraction, multiplication, etc. 

    +	Add two operands or unary plus	x + y+ 2
    -	Subtract right operand from the left or unary minus	x - y- 2
    *	Multiply two operands	x * y
    /	Divide left operand by the right one (always results into float)	x / y
    %	Modulus - remainder of the division of left operand by the right	x % y (remainder of x/y)
    //	Floor division - division that results into whole number adjusted to the left in the number line	x // y
    **	Exponent - left operand raised to the power of right	x**y (x to the power y)

    Such as:


"""


# Arithmatic Operator:

a , b = 20 , 10;

print(a+b) # Addition

print(a-b) # Subtraction

print(a*b) # Multiplication

print(a/b) # Division

print(a%b) # Modulas: That returns the value after the float point part

print(a//b)  # Floor division - division that results into whole number adjusted to the left in the number line

print(a**b) # Power: That returns the power value of the input

